/*
Faça um programa que calcule e exiba na tela a soma e o produto de dois números reais,
sendo um deles 91,37 e o outro 7,351. Exiba o resultado na tela.
 */
public class Exercicio1 {
    public static void main(String[] args) {
        double num1 = 91.37;
        double num2 = 7.351;
        double Produto = num1 * num2;
        double Soma = num1 + num2;
        System.out.println("A soma dos números é: " + Soma + " e o produto, " + Produto);
    }
}

